class PAM4RSASCA2k(nn.Module):
    def __init__(self, c1=3, c2=96, kernel_size=7, reduction=32, k=64, num_heads=4):
        super(PAM4RSASCA2k, self).__init__()
        self.conv = None
        if c1 != c2:
            self.conv = Conv(c1, c2)
        self.d = max(8, c2 // reduction)
        self.topk_sa = k
        self.pos1 = Conv(c2, c2, k=7, p=(7 - 1) // 2, g=c2)
        self.pos2 = Conv(c2, self.d, 1, 1)
        self.pos_sa = nn.Conv2d(self.d, c2, kernel_size=1)
        self.softmax = nn.Softmax(dim=1)
        self.ma = nn.MultiheadAttention(embed_dim=self.d, num_heads=num_heads)
        self.sigmoid = nn.Sigmoid()
        self.norm = nn.LayerNorm(self.d)
        self.out = Conv(c2, c2)
        self.mlp = RSAS(c2,kernel_size,kernel_size)
        self.sma = RCA(self.d) # b, 1 ,2h, 2w
        self.avg_pool = nn.AvgPool2d(7, 2, 3)

    def forward(self, x):
        if self.conv is not None:
            x = self.conv(x)
        b, c, h, w = x.size()
        xpos1 = self.pos1(x)  # b ,c,w,h
        xpos = self.pos2(xpos1)  # b ,d,w,h

        mask = self.sigmoid(self.avg_pool(self.sma(xpos)))  # b, 1 ,2h, 2w -> HW
        xpos = xpos.permute(0, 2, 3, 1).contiguous().view(b, h * w, -1)  # 1
        score = self.softmax(xpos)

        select_tgt_index = torch.topk(score, self.topk_sa, dim=1)[1]
        select_x = torch.gather(xpos, 1, select_tgt_index)  # n
        select_xpos = select_x

        select_tgt_indexhw = select_tgt_index  # bkc
        coords_h = select_tgt_indexhw // w  # Ë°åÂè∑ (H ÊñπÂêë)  # bkc
        coords_w = select_tgt_indexhw % w  # ÂàóÂè∑ (W ÊñπÂêë)   # bkc
        diff_h = coords_h.unsqueeze(2) - coords_h.unsqueeze(1)  # [b, k, k, 1]
        diff_w = coords_w.unsqueeze(2) - coords_w.unsqueeze(1)  # [b, k, k, 1]
        d = torch.sqrt(diff_h ** 2 + diff_w ** 2 + 1e-6)  # [b, k, k, 1]

        sigma = 0.5  # ÊéßÂà∂Ë°∞ÂáèÈÄüÁéá
        w1 = d / sigma  # [b, k, k, 1]ÔºåË∑ùÁ¶ªË∂äËøúÊùÉÈáçË∂äÂ∞è
        w1 = w1 / w1.sum(dim=2, keepdim=True)  # [b, k, k, 1]

        select_x = torch.pow(self.sigmoid(select_x.unsqueeze(1)), w1)  # [b, k, k, 1]ÔºåÊØè‰∏™ÁÇπÁöÑÁâπÂæÅÂèóÂÖ∂ÈÇªÂ±ÖÁÇπÂä†ÊùÉÂΩ±Âìç
        select_x = self.sigmoid(select_x.prod(dim=2))

        tgt = self.ma(select_x + select_xpos, select_xpos, select_xpos)[0]
        tgt = self.norm(tgt)
        xpos = xpos.scatter(1, select_tgt_index, tgt.to(x.dtype))
        xpos = xpos.view(b, -1, h, w)
        xpos = self.pos_sa(xpos) * mask  # c

        x = x + self.out(xpos)
        x = x + self.mlp(x)
        return x



class RSAS(nn.Module):
    """Context Anchor Attention"""
    def __init__(self, c2, h_kernel_size= 9,v_kernel_size= 9,): #9 76 591 # 11 58.67 75.22
        super().__init__()
        self.h_conv = Conv(c2, c2, (1, h_kernel_size), 1,
                                 (0, h_kernel_size // 2), g=c2,)
        self.v_conv = Conv(c2, c2, (v_kernel_size, 1), 1,
                                 (v_kernel_size // 2, 0), g=c2)
        self.conv2 = Conv(c2, c2, 1, 1, 0)
        self.act = nn.Sigmoid()

    def forward(self, x):
        attn_factor = self.act(self.conv2(self.v_conv(self.h_conv(x))))
        return attn_factor * x



class RCA(nn.Module):
    def __init__(self,c2, kernel_size= 3,up_kernel=2):
        super().__init__()
        self.up_kernel = up_kernel
        self.content_encoder = nn.Conv2d(c2,self.up_kernel * self.up_kernel,kernel_size,
            padding= (kernel_size// 2),dilation=1,groups=1)

    def kernel_normalizer(self, mask):
        mask = F.pixel_shuffle(mask, self.up_kernel)
        n, mask_c, h, w = mask.size()
        mask = mask.view(n, mask_c, h, w).contiguous()
        return mask

    def forward(self, x):
        mask = self.content_encoder(x)
        mask = self.kernel_normalizer(mask)
        return mask


class Bottleneck(nn.Module):
    """Standard bottleneck."""

    def __init__(self, c1, c2, shortcut=True, g=1, k=(3, 3), e=0.5):
        """Initializes a standard bottleneck module with optional shortcut connection and configurable parameters."""
        super().__init__()
        c_ = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, k[0], 1)
        self.cv2 = Conv(c_, c2, k[1], 1, g=g)
        self.add = shortcut and c1 == c2

    def forward(self, x):
        """Applies the YOLO FPN to input data."""
        return x + self.cv2(self.cv1(x)) if self.add else self.cv2(self.cv1(x))


class BottleneckCSP(nn.Module):
    """CSP Bottleneck https://github.com/WongKinYiu/CrossStagePartialNetworks."""

    def __init__(self, c1, c2, n=1, shortcut=True, g=1, e=0.5):
        """Initializes the CSP Bottleneck given arguments for ch_in, ch_out, number, shortcut, groups, expansion."""
        super().__init__()
        c_ = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = nn.Conv2d(c1, c_, 1, 1, bias=False)
        self.cv3 = nn.Conv2d(c_, c_, 1, 1, bias=False)
        self.cv4 = Conv(2 * c_, c2, 1, 1)
        self.bn = nn.BatchNorm2d(2 * c_)  # applied to cat(cv2, cv3)
        self.act = nn.SiLU()
        self.m = nn.Sequential(*(Bottleneck(c_, c_, shortcut, g, e=1.0) for _ in range(n)))

    def forward(self, x):
        """Applies a CSP bottleneck with 3 convolutions."""
        y1 = self.cv3(self.m(self.cv1(x)))
        y2 = self.cv2(x)
        return self.cv4(self.act(self.bn(torch.cat((y1, y2), 1))))


class ResNetBlock(nn.Module):
    """ResNet block with standard convolution layers."""

    def __init__(self, c1, c2, s=1, e=4):
        """Initialize convolution with given parameters."""
        super().__init__()
        c3 = e * c2
        self.cv1 = Conv(c1, c2, k=1, s=1, act=True)
        self.cv2 = Conv(c2, c2, k=3, s=s, p=1, act=True)
        self.cv3 = Conv(c2, c3, k=1, act=False)
        self.shortcut = nn.Sequential(Conv(c1, c3, k=1, s=s, act=False)) if s != 1 or c1 != c3 else nn.Identity()

    def forward(self, x):
        """Forward pass through the ResNet block."""
        return F.relu(self.cv3(self.cv2(self.cv1(x))) + self.shortcut(x))


